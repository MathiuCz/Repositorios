
"""4.Hallar el volumen de una esfera, cada dato requerido para hallar el volumen debe
estar en una variable. Mostrar el volumen por pantalla."""

#Volumen de una esfera 4/3(pi(radio**3))

#Valor aproximado de pi
pi = 3.14
#Radio elegido
radio = 6

Volumen_esfera = (4 * pi * (radio ** 3)) / 3

print("El volumen de la esfera de radio {} es {} unidades cubicas.".format(radio, Volumen_esfera))